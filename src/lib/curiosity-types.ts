/** 全タイプをフラットに取得 */
export const allCuriosityTypes: CuriosityType[] = curiosityZones.flatMap(z => z.types);

/** IDからタイプを取得 */
export function getCuriosityTypeById(id: string): CuriosityType | undefined {
  return allCuriosityTypes.find(t => t.id === id);
}

/** IDからタイプ名を取得（表示用） */
export function getCuriosityTypeName(id: string): string {
  return getCuriosityTypeById(id)?.name ?? '不明なタイプ';
}

/** 有効な好奇心タイプIDの一覧 */
export const validCuriosityTypeIds = allCuriosityTypes.map(t => t.id);
